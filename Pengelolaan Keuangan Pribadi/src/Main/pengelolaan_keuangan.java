    private void viewData() {
        DefaultTableModel m = (DefaultTableModel) TabelTransaksi.getModel();
        m.setRowCount(0);
        try {
            String Q = "SELECT * FROM transaksi";
            Connection K = Koneksi.conn();
            PreparedStatement pst = K.prepareStatement(Q);
            ResultSet rs = pst.executeQuery();
            while (rs.next()) {
                m.addRow(new Object[]{
                    rs.getInt("id"),
                    rs.getString("tanggal"),
                    rs.getString("jenis_transaksi"),
                    rs.getInt("nominal"),
                    rs.getString("keterangan")
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data: " + e.getMessage());
        }
    }
    
    private void updateTotal() {
        try {
            String Pemasukan = "SELECT SUM(nominal) FROM transaksi WHERE jenis_transaksi='Pemasukan'";
            String Pengeluaran = "SELECT SUM(nominal) FROM transaksi WHERE jenis_transaksi='Pengeluaran'";
            Connection K = Koneksi.conn();
            PreparedStatement pst1 = K.prepareStatement(Pemasukan);
            PreparedStatement pst2 = K.prepareStatement(Pengeluaran);
            ResultSet rs1 = pst1.executeQuery();
            ResultSet rs2 = pst2.executeQuery();

            int totalPemasukan = rs1.next() ? rs1.getInt(1) : 0;
            int totalPengeluaran = rs2.next() ? rs2.getInt(1) : 0;
            int totalSaldo = totalPemasukan - totalPengeluaran;

            lblTotalPemasukan.setText("Rp." + totalPemasukan);
            lblTotalPengeluaran.setText("Rp." + totalPengeluaran);
            lblTotalSaldo.setText("Rp." + totalSaldo);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal menghitung total: " + e.getMessage());
        }
    }

