    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String tanggal = sdf.format(txtTanggal.getDate());
        String jenis_transaksi = cmbJenisTransaksi.getSelectedItem().toString();
        int nominal = Integer.parseInt(txtNominal.getText());
        String keterangan = txtKeterangan.getText();

        try {
            String Q = "INSERT INTO transaksi (tanggal, jenis_transaksi, nominal, keterangan) VALUES (?, ?, ?, ?)";
            Connection K = Koneksi.conn();
            PreparedStatement pst = K.prepareStatement(Q);
            pst.setString(1, tanggal);
            pst.setString(2, jenis_transaksi);
            pst.setInt(3, nominal);
            pst.setString(4, keterangan);
            pst.executeUpdate();

            txtTanggal.setDate(null);
            cmbJenisTransaksi.setSelectedIndex(0);
            txtNominal.setText("");
            txtKeterangan.setText("");

            DefaultTableModel m = (DefaultTableModel) TabelTransaksi.getModel();
            m.setRowCount(0);

            updateTotal();
            JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal menambah data: " + e.getMessage());
        }
    }//GEN-LAST:event_btnTambahActionPerformed

